static int gc4663_sensor_vts = 3200;
static int sensor_s_shutter(int id, unsigned int intt_long, unsigned int intt_short)
{
	unsigned int intt_long_h, intt_long_l,intt_short_h,intt_short_l;
	unsigned int short_exp_max = gc4663_sensor_vts - 1456 - 20 - 16 - 8, long_exp_max = 0; //20fps - 900

	if (intt_long <= 1)
		intt_long = 1;
	if (intt_short < 1)
		intt_short = 1;

	if (intt_short >= short_exp_max)
		intt_short = short_exp_max;
	long_exp_max = gc4663_sensor_vts - intt_short - 16;
	if (intt_long >= long_exp_max)
		intt_long = long_exp_max;

	intt_long_l = intt_long & 0xff;
	intt_long_h = (intt_long >> 8) & 0x3f;
	intt_short_l = intt_short & 0xff;
	intt_short_h = (intt_short >> 8) & 0x3f;

	sensor_write(id, 0x0202, intt_long_h);
	sensor_write(id, 0x0203, intt_long_l);
	sensor_dbg("sensor_set_long_exp = %d line Done!\n", intt_long);
	sensor_write(id, 0x0200, intt_short_h);
	sensor_write(id, 0x0201, intt_short_l);
	sensor_dbg("sensor_set_short_exp = %d line Done!\n", intt_short);

	return 0;
}

static int sensor_s_exp(int id, unsigned int exp_val, unsigned int exp_short)
{
	int tmp_exp_val = exp_val / 16, tmp_exp_val_s = exp_short / 16;
	int isp_wdr_mode;

	if (!full_size) {
		isp_wdr_mode = ISP_NORMAL_MODE;
	} else {
		if (isp_get_cfg[id].sensor_wdr_on)
			isp_wdr_mode = ISP_DOL_WDR_MODE;
		else
			isp_wdr_mode = ISP_NORMAL_MODE;
	}

	if (isp_wdr_mode == ISP_DOL_WDR_MODE) {
		sensor_dbg("Sensor in WDR mode, HDR_RATIO = %d\n", exp_val / exp_short);
		//exp_short = tmp_exp_val / HDR_RATIO;
		sensor_s_shutter(id, tmp_exp_val, tmp_exp_val_s);
	} else {
		sensor_dbg("exp_val:%d\n", exp_val);
		sensor_write(id, 0x202, (tmp_exp_val >> 8) & 0xFF);
		sensor_write(id, 0x203, (tmp_exp_val & 0xFF));
		if (!full_size) {
			sensor_write(id, 0x0200, (tmp_exp_val_s >> 8) & 0xFF);
			sensor_write(id, 0x0201, (tmp_exp_val_s & 0xFF));
		}
	}

	return 0;
}

